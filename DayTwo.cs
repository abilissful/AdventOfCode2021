    public int GetCompositePosition()
    {
        commands = ParseDirections(input);

        foreach (var command in commands)
        {
            if (command.direction == "forward")
            horizontalPosition += command.distance;

            if (command.direction == "down")
            verticalPosition += command.distance;

            if (command.direction == "up")
            verticalPosition -= command.distance;
        }

        return horizontalPosition * verticalPosition;
    }

    public int GetCompositeAimPosition()
    {
        commands = ParseDirections(input);

        foreach (var command in commands)
        {
            if (command.direction == "forward")
            {
                horizontalPosition += command.distance;
                verticalPosition += (aim * command.distance);
            }

            if (command.direction == "down")
            aim += command.distance;

            if (command.direction == "up")
            aim -= command.distance;
        }

        return horizontalPosition * verticalPosition;
    }

    public List<Command> ParseDirections(string input)
    {
        var asStrings = input.Split(Environment.NewLine);
        var commands = new List<Command>();

        for(var i = 0; i < asStrings.Count(); i++)
        {
            var direction = asStrings[i].Split(" ").First();
            var distance = asStrings[i].Split(" ").Last();

            commands.Add(new Command(direction, Int32.Parse(distance)));
        }

        return commands;
    }

